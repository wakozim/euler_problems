{
#define X(name) "    "#name"\r\n"
    std::string task = 
        "The four adjacent digits in the 1000-digit "
        "number that have the greatest product are 9×9×8×9 = 5832.\r\n"
        NUMBER"\r\n"
        "Find the thirteen adjacent digits in "
        "the 1000-digit number that have the greatest "
        "product. What is the value of this product?";
#undef X
    
#define X(name) #name
    char number[] = {
        NUMBER
    }; 
#undef X
    
    long int greatest_product = 0;
    for (size_t i = 0; i < sizeof(number) - DIGITS_COUNT; ++i) {
        long int product = 1;
        for (size_t j = i; j < i + DIGITS_COUNT; ++j) {
            product *= number[j] - '0';
        }
        if (product > greatest_product) greatest_product = product; 
    } 

    print_answer(task, greatest_product);
    return 0;
}
